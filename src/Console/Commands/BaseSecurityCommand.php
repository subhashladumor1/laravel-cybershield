        $this->line("  <box>в”Ң" . str_repeat('в”Җ', $width) . "в”җ</box>");
        $this->line("  <box>в”Ӯ</box><acc>{$inner}</acc>" . str_repeat(' ', $pad) . "<box>в”Ӯ</box>");
