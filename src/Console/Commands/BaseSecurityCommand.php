            $this->line("  <box>в•‘</box>  <fail>{$verdict}</fail>" . str_repeat(' ', max(1, 68 - mb_strlen($verdict))) . "<box>в•‘</box>");
            $this->line("  <box>в•ҡ" . str_repeat('в•җ', 70) . "в•қ</box>");
