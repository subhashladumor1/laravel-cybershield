    protected function runScanStep(string $label, callable $callback): mixed
    {
        $dots = max(2, 52 - mb_strlen($label));
        $this->output->write("  <box>в”Ӯ</box> <hi>{$label}</hi><dim>" . str_repeat('В·', $dots) . "</dim>");

        $result = $callback();

        if ($result === true || (is_array($result) && count($result) === 0)) {
            $this->line(" <ok>вң” PASS</ok>");
            return true;
        } else {
            $count = is_array($result) ? count($result) : 1;
            $this->line(" <warn>вҡ  {$count} ISSUE" . ($count > 1 ? 'S' : '') . "</warn>");
            return $result;
        }
    }

    protected function printSummary(array $findings): void
    {
        $totalIssues = array_sum(array_map(
            fn($v) => is_array($v) ? count($v) : 0,
            $findings
        ));

        $passed = count(array_filter($findings, fn($v) => $v === true || (is_array($v) && count($v) === 0)));
        $failed = count($findings) - $passed;
        $score = count($findings) > 0
            ? min(100, (int) (($passed / count($findings)) * 100))
            : 100;

        $scoreColor = $score >= 90 ? 'ok' : ($score >= 70 ? 'warn' : 'fail');
        $scoreBar = $this->buildScoreBar($score);

        $this->line('');
        $this->line("  <box>в•”" . str_repeat('в•җ', 70) . "в•—</box>");
        $this->line("  <box>в•‘</box>  <hi>в—Ҳ  SECURITY SCAN RESULTS</hi>" . str_repeat(' ', 45) . "<box>в•‘</box>");
        $this->line("  <box>в• " . str_repeat('в•җ', 70) . "в•Ј</box>");

        // Score line
        $scoreStr = "  Score: <{$scoreColor}>{$score}/100</{$scoreColor}>";
        $this->line("  <box>в•‘</box>  {$scoreStr}   {$scoreBar}  <dim>({$passed} passed В· {$failed} failed)</dim>  <box>в•‘</box>");
        $this->line("  <box>в• " . str_repeat('в•җ', 70) . "в•Ј</box>");

        // Category rows
        foreach ($findings as $category => $issues) {
            if (!is_array($issues))
                continue;

            $count = count($issues);
            $catPad = str_repeat(' ', max(1, 30 - mb_strlen($category)));

            if ($count === 0) {
                $statusTag = "<ok>  вң”  PASS  </ok>";
            } elseif ($count <= 3) {
                $statusTag = "<warn>  вҡ   WARN  </warn>";
            } else {
                $statusTag = "<fail>  вң—  FAIL  </fail>";
            }

            $countStr = $count > 0 ? "<warn>{$count} issue" . ($count > 1 ? 's' : '') . "</warn>" : "<ok>none</ok>";
            $this->line("  <box>в•‘</box>  {$statusTag}  <hi>{$category}</hi>{$catPad}{$countStr}" . str_repeat(' ', max(1, 16 - mb_strlen((string) $count) - 6 - ($count > 1 ? 1 : 0))) . "<box>в•‘</box>");

            foreach (array_slice($issues, 0, 3) as $issue) {
                $issueStr = is_array($issue) ? implode(', ', $issue) : (string) $issue;
                $truncated = mb_strlen($issueStr) > 60 ? mb_substr($issueStr, 0, 57) . '...' : $issueStr;
                $this->line("  <box>в•‘</box>       <dim>в””в”Җ</dim> <fail>{$truncated}</fail>" . str_repeat(' ', max(1, 58 - mb_strlen($truncated))) . "<box>в•‘</box>");
            }
            if (count($issues) > 3) {
                $remaining = count($issues) - 3;
                $this->line("  <box>в•‘</box>         <dim>вҖҰ and {$remaining} more issue" . ($remaining > 1 ? 's' : '') . "</dim>" . str_repeat(' ', max(1, 55 - mb_strlen((string) $remaining))) . "<box>в•‘</box>");
            }
        }

        $this->line("  <box>в• " . str_repeat('в•җ', 70) . "в•Ј</box>");

        // Final verdict
        if ($totalIssues === 0) {
            $verdict = "  вң”  ALL CHECKS PASSED вҖ” Application is secure!";
            $this->line("  <box>в•‘</box>  <ok>{$verdict}</ok>" . str_repeat(' ', max(1, 68 - mb_strlen($verdict))) . "<box>в•‘</box>");
            $this->line("  <box>в•ҡ" . str_repeat('в•җ', 70) . "в•қ</box>");
            $this->line('');
            $this->line("  <dim>в”Ңв”Җ Tip в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”җ</dim>");
            $this->line("  <dim>в”Ӯ  Run  php artisan cybershield  to open the control panel.         в”Ӯ</dim>");
            $this->line("  <dim>в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ</dim>");
